//Greatest in two 
#include<iostream>
using namespace std;
int main()
{
	int a,b;
	cout<<"Enter first value  : ";
	cin>>a;
	cout<<"\nEnter second value : ";
	cin>>b;
	if(a>b)
	{
		cout<<"\nThe first value is greatest : ";
		cout<<a;
	}
	else
	{
		cout<<"\nThe second value is greatest : ";
		cout<<b;
	}
	
	
	return 0;
}
